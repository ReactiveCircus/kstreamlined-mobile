/**
 * Generates FIR declaration for the `<NavEntryContent>_NavEntryInstaller` class.
 *
 * Metro IR expects synthetic MetroFactory declarations generated by Metro FIR,
 * using internal `GeneratedDeclarationKey` data objects for lookups.
 * Since FIR code generated by one plugin cannot be processed by other plugin's FIR,
 * we generate the same synthetic `MetroFactory` class in our own FIR using the same
 * `GeneratedDeclarationKey` instances used my Metro FIR so Metro IR can find the declarations.
 *
 * This won't be required once Metro moves factory generation to IR.
 */
@Suppress("TooManyFunctions")
internal class RouteBindingClassGenerator(session: FirSession) : FirDeclarationGenerationExtension(session) {
    private val hasRouteBindingAnnotation = LookupPredicate.create {
        annotated(ClassIds.RouteBinding.Annotation.asSingleFqName())
    }

    private val matchedFunctions: FirCache<Unit, List<FirNamedFunctionSymbol>, Nothing?> =
        session.firCachesFactory.createCache { _, _ ->
            session.predicateBasedProvider.getSymbolsByPredicate(hasRouteBindingAnnotation)
                .filterIsInstance<FirNamedFunctionSymbol>()
        }

    override fun FirDeclarationPredicateRegistrar.registerPredicates() {
        register(hasRouteBindingAnnotation)
    }

    @ExperimentalTopLevelDeclarationsGenerationApi
    override fun getTopLevelClassIds(): Set<ClassId> {
        return matchedFunctions.getValue(Unit).map { function ->
            val packageFqName = function.callableId.packageName
            val classNameSuffix = ClassIds.RouteBinding.NavEntryInstaller.shortClassName.asString()
            val className = Name.identifier("${function.name.asString()}_$classNameSuffix")
            ClassId(packageFqName, className)
        }.toSet()
    }

    @ExperimentalTopLevelDeclarationsGenerationApi
    override fun generateTopLevelClassLikeDeclaration(classId: ClassId): FirClassLikeSymbol<*>? {
        return createTopLevelClass(
            classId = classId,
            key = RouteBindingKeys.NavEntryInstallerClassDeclaration,
        ) {
            superType(ClassIds.RouteBinding.NavEntryInstaller.createConeType(session))
        }.apply {
            replaceAnnotations(annotations + buildDeprecatedAnnotation(session) + buildContributesIntoSetAnnotation())
            replaceDeprecationsProvider(getDeprecationsProvider(session))
        }.symbol
    }

    private fun buildContributesIntoSetAnnotation(): FirAnnotation = buildAnnotation {
        annotationTypeRef = buildResolvedTypeRef {
            coneType = ClassIds.Metro.ContributesIntoSet.createConeType(session)
        }
        argumentMapping = buildAnnotationArgumentMapping {
            mapping[Name.identifier("scope")] = buildGetClassCall {
                val appScopeSymbol = session.symbolProvider.getClassLikeSymbolByClassId(ClassIds.Metro.AppScope)!!
                val appScopeConeType = appScopeSymbol.classId.constructClassLikeType()
                val kClassSymbol = session.symbolProvider.getClassLikeSymbolByClassId(StandardClassIds.KClass)!!
                val kClassType = kClassSymbol.classId.constructClassLikeType(arrayOf(appScopeConeType))
                coneTypeOrNull = kClassType
                argumentList = buildArgumentList {
                    arguments += buildResolvedQualifier {
                        coneTypeOrNull = appScopeConeType
                        symbol = appScopeSymbol
                        packageFqName = ClassIds.Metro.AppScope.packageFqName
                        relativeClassFqName = ClassIds.Metro.AppScope.relativeClassName
                        resolvedToCompanionObject = false
                    }
                }
            }
        }
    }

    override fun getCallableNamesForClass(classSymbol: FirClassSymbol<*>, context: MemberGenerationContext): Set<Name> {
        // for <NavEntryContent>_NavEntryInstaller class
        if (classSymbol.hasOrigin(RouteBindingKeys.NavEntryInstallerClassDeclaration)) {
            return setOf(SpecialNames.INIT, Name.identifier("install"))
        }
        // for nested MetroFactory object
        if (classSymbol.hasOrigin(RouteBindingKeys.MetroFactoryClassDeclaration)) {
            return setOf(SpecialNames.INIT, Name.identifier("create"), Name.identifier("newInstance"))
        }
        return emptySet()
    }

    override fun generateConstructors(context: MemberGenerationContext): List<FirConstructorSymbol> {
        val classSymbol = context.owner

        // for <NavEntryContent>_NavEntryInstaller class
        if (classSymbol.hasOrigin(RouteBindingKeys.NavEntryInstallerClassDeclaration)) {
            return listOf(
                createConstructor(
                    owner = classSymbol,
                    key = RouteBindingKeys.Default,
                    isPrimary = true,
                    generateDelegatedNoArgConstructorCall = true,
                ).symbol,
            )
        }

        // for nested MetroFactory object
        if (classSymbol.hasOrigin(RouteBindingKeys.MetroFactoryClassDeclaration)) {
            return listOf(
                createConstructor(
                    owner = classSymbol,
                    key = RouteBindingKeys.Default,
                    isPrimary = true,
                    generateDelegatedNoArgConstructorCall = true,
                    config = { visibility = Visibilities.Private },
                ).symbol,
            )
        }

        return emptyList()
    }

    override fun generateFunctions(
        callableId: CallableId,
        context: MemberGenerationContext?,
    ): List<FirNamedFunctionSymbol> {
        if (context == null) return emptyList()
        val classSymbol = context.owner
        val classId = classSymbol.classId

        // install function
        if (classSymbol.hasOrigin(RouteBindingKeys.NavEntryInstallerClassDeclaration)) {
            if (callableId.callableName.asString() != "install") {
                return emptyList()
            }
            return listOf(generateInstallFunction(classSymbol))
        }

        // create and newInstance functions in MetroFactory
        if (classSymbol.hasOrigin(RouteBindingKeys.MetroFactoryClassDeclaration)) {
            val outerClassId = classId.outerClassId!!
            val outerClassType = outerClassId.createConeType(session)
            val factoryType = ClassIds.Metro.Factory.constructClassLikeType(arrayOf(outerClassType))

            return when (callableId.callableName.asString()) {
                "create" -> listOf(
                    createMemberFunction(
                        owner = classSymbol,
                        key = RouteBindingKeys.MetroFactoryCreateFunction,
                        name = Name.identifier("create"),
                        returnType = factoryType,
                    ).symbol,
                )

                "newInstance" -> listOf(
                    createMemberFunction(
                        owner = classSymbol,
                        key = RouteBindingKeys.MetroFactoryNewInstanceFunction,
                        name = Name.identifier("newInstance"),
                        returnType = outerClassType,
                    ).symbol,
                )

                else -> emptyList()
            }
        }

        return emptyList()
    }

    private fun generateInstallFunction(classSymbol: FirClassSymbol<*>): FirNamedFunctionSymbol {
        val navKeyType = ClassIds.Nav3.NavKey.createConeType(session)
        val entryProviderScopeType = ClassIds.Nav3.EntryProviderScope.constructClassLikeType(arrayOf(navKeyType))
        val sharedTransitionScopeType = ClassIds.Compose.SharedTransitionScope.createConeType(session)
        val navBackStackType = ClassIds.Nav3.NavBackStack.constructClassLikeType(arrayOf(navKeyType))
        val unitType = session.builtinTypes.unitType.coneType

        return createMemberFunction(
            owner = classSymbol,
            key = RouteBindingKeys.InstallFunctionDeclaration,
            name = Name.identifier("install"),
            returnType = unitType,
        ) {
            contextReceiver(entryProviderScopeType)
            contextReceiver(sharedTransitionScopeType)
            valueParameter(Name.identifier("backStack"), navBackStackType)
            status {
                isOverride = true
            }
        }.symbol
    }

    override fun getNestedClassifiersNames(
        classSymbol: FirClassSymbol<*>,
        context: NestedClassGenerationContext,
    ): Set<Name> {
        if (!classSymbol.hasOrigin(RouteBindingKeys.NavEntryInstallerClassDeclaration)) {
            return emptySet()
        }
        return setOf(MetroFactoryName)
    }

    override fun generateNestedClassLikeDeclaration(
        owner: FirClassSymbol<*>,
        name: Name,
        context: NestedClassGenerationContext,
    ): FirClassLikeSymbol<*>? {
        if (name != MetroFactoryName || !owner.hasOrigin(RouteBindingKeys.NavEntryInstallerClassDeclaration)) {
            return null
        }

        val ownerType = owner.defaultType()
        val factoryType = ClassIds.Metro.Factory.constructClassLikeType(arrayOf(ownerType))

        return createNestedClass(
            owner = owner,
            key = RouteBindingKeys.MetroFactoryClassDeclaration,
            name = MetroFactoryName,
            classKind = ClassKind.OBJECT,
        ) {
            superType(factoryType)
        }.apply {
            replaceAnnotations(annotations + buildDeprecatedAnnotation(session))
            replaceDeprecationsProvider(getDeprecationsProvider(session))
        }.symbol
    }

    private fun buildDeprecatedAnnotation(session: FirSession): FirAnnotation = buildAnnotation {
        val deprecatedAnnotation = session.symbolProvider
            .getClassLikeSymbolByClassId(StandardClassIds.Annotations.Deprecated) as FirRegularClassSymbol

        annotationTypeRef = deprecatedAnnotation.defaultType().toFirResolvedTypeRef()
        argumentMapping = buildAnnotationArgumentMapping {
            mapping[Name.identifier("message")] = buildLiteralExpression(
                source = null,
                kind = ConstantValueKind.String,
                value = "This synthesized declaration should not be used directly",
                setType = true,
            )
            mapping[Name.identifier("level")] =
                buildEnumEntryDeserializedAccessExpression {
                    enumClassId = StandardClassIds.DeprecationLevel
                    enumEntryName = Name.identifier("HIDDEN")
                }.toQualifiedPropertyAccessExpression(session)
        }
    }

    private fun FirBasedSymbol<*>.hasOrigin(key: GeneratedDeclarationKey): Boolean {
        val origin = origin
        return origin is FirDeclarationOrigin.Plugin && origin.key == key
    }

    companion object {
        private val MetroFactoryName = Name.identifier("MetroFactory")
    }
}
